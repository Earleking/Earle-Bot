class Pokemon {
    constructor() {
        this.id;
        this.type;
        this.gender;
        this.hp;
        this.attack;
        this.defense;
        this.sAttack;
        this.sDefense;
        this.speed;
        this.moves;
    }
    setHP(newHp) {
        this.hp = newHP;
    }
    setAttack(newAttack) {
        attack = newAttack;
    }
    setDefense(newDefense) {
        defense = newDefense;
    }
    setSAttack(newSAttack) {
        sAttack = newSAttack;
    }
    setSDefense(newSDefense) {
        sDefense = newSDefense;
    }
    setSpeed(newSpeed) {
        speed = newSpeed;
    }
    getID() {
        return id;
    }
    getAttack() {
        return attack;
    }
    getDefense() {
        return defense;
    }
    getSAttack() {
        return sAttack;
    }
    getSDefense() {
        return sDefense;
    }
    getSpeed() {
        return speed;
    }

}